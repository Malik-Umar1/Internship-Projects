// Assign my age to the variable myAge.
let myAge = 21;

// Assign the initial 2 years of a dog's life to earlyYears.
let earlyYears = 2;

earlyYears = earlyYears * 10.5;

// Subtract 2 from myAge to account for the first 2 years of a human's life.
laterYears = myAge-2;
 
// Multiply laterYears by 4 to account for the remaining years of a human's life.
laterYears = laterYears * 4;

// Add earlyYears and laterYears to find age in dog years.
let myAgeInDogYears = earlyYears + laterYears;

// Convert my name to lowercase and store the result in myName.
let myName = 'Umar'.toLowerCase();

// Display my name and my age in dog years.
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)